
public class CalculadoraDigital extends Calculadora{

	@Override
	public void suma(Double i, Double j) {
		System.out.println(i + j);
		
	}

	@Override
	public void resta(Double i, Double j) {
		System.out.println(i - j);
		
	}

	@Override
	public void multiplicacion(Double i, Double j) {
		System.out.println(i * j);
		
	}

	@Override
	public void division(Double i, Double j) {
		System.out.println(i / j);
		
	}

	

}
